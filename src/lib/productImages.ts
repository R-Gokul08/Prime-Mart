// Get product image by name (fuzzy matching)
export function getProductImage(name: string, category?: string): string | undefined {
  const lowercaseName = name.toLowerCase();
  
  // Direct match
  if (productImages[lowercaseName]) {
    return productImages[lowercaseName];
  }
  
  // Partial match
  for (const [key, url] of Object.entries(productImages)) {
    if (lowercaseName.includes(key) || key.includes(lowercaseName)) {
      return url;
    }
  }
  
  // Category fallback
  if (category && categoryImages[category]) {
    return categoryImages[category];
  }
  
  return undefined;
}

