// Get product image by name (fuzzy matching)
export function getProductImage(name: string, category?: string): string | undefined {
  const lowercaseName = name.toLowerCase().trim();

  // Direct match
  if (productImages[lowercaseName]) {
    return productImages[lowercaseName];
  }

  // Partial match - check if product name contains any keyword
  for (const [key, url] of Object.entries(productImages)) {
    if (lowercaseName.includes(key) || key.includes(lowercaseName)) {
      return url;
    }
  }

  // Word-by-word matching (skip short common words)
  const words = lowercaseName.split(/[\s,]+/);
  for (const word of words) {
    if (word.length > 3 && productImages[word]) {
      return productImages[word];
    }
  }

  // Try matching any word in product name keys
  for (const word of words) {
    if (word.length > 3) {
      for (const [key, url] of Object.entries(productImages)) {
        if (key.includes(word)) {
          return url;
        }
      }
    }
  }

  // Category fallback
  if (category && categoryImages[category]) {
    return categoryImages[category];
  }

  return undefined;
}

// Reliable fallback using picsum (works unlike deprecated source.unsplash.com)
export function getGoogleImageUrl(productName: string): string {
  // Use a deterministic seed based on product name for consistent images
  const seed = productName.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
  return `https://picsum.photos/seed/${seed}/200/200`;
}

